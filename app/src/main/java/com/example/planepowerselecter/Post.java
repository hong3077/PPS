package com.example.planepowerselecter;


public class Post {

    private String product_name;

    private String voltage;

    private int power;

    private String purpose;

    private int cost;

    public String getProduct_name(){
        return product_name;
    }

    public String getVoltage(){
        return voltage;
    }

    public int getPower(){
        return power;
    }

    public String getPurpose(){
        return purpose;
    }

    public int getCost(){
        return cost;
    }
}